#!/bin/bash

# –ê–≤—Ç–æ–º–∞—Ç–∏—á–Ω–µ –æ–Ω–æ–≤–ª–µ–Ω–Ω—è Git —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä—ñ—é
# –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è: ./update.sh –∞–±–æ ./update.sh "–≤–∞—à–µ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –∫–æ–º—ñ—Ç—É"

# –ö–æ–ª—å–æ—Ä–∏ –¥–ª—è –≤–∏–≤–æ–¥—É
GREEN='\033[0;32m'
BLUE='\033[0;34m'
RED='\033[0;31m'
NC='\033[0m' # No Color

echo -e "${BLUE}üîÑ –ü–æ—á–∞—Ç–æ–∫ –æ–Ω–æ–≤–ª–µ–Ω–Ω—è —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä—ñ—é...${NC}"

# –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ —á–∏ —î –∑–º—ñ–Ω–∏ –¥–ª—è –∫–æ–º—ñ—Ç—É
if git diff --quiet && git diff --staged --quiet; then
    echo -e "${BLUE}‚ÑπÔ∏è  –ù–µ–º–∞—î –∑–º—ñ–Ω –¥–ª—è –∫–æ–º—ñ—Ç—É${NC}"
    exit 0
fi

# –î–æ–¥–∞–≤–∞–Ω–Ω—è –≤—Å—ñ—Ö —Ñ–∞–π–ª—ñ–≤
echo -e "${BLUE}üìÅ –î–æ–¥–∞–≤–∞–Ω–Ω—è —Ñ–∞–π–ª—ñ–≤...${NC}"
git add .

# –ö–æ–º—ñ—Ç –∑ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è–º (–≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ –ø–µ—Ä–µ–¥–∞–Ω–∏–π –ø–∞—Ä–∞–º–µ—Ç—Ä –∞–±–æ "update" –∑–∞ –∑–∞–º–æ–≤—á—É–≤–∞–Ω–Ω—è–º)
COMMIT_MESSAGE=${1:-"update"}
echo -e "${BLUE}üíæ –°—Ç–≤–æ—Ä–µ–Ω–Ω—è –∫–æ–º—ñ—Ç—É: \"${COMMIT_MESSAGE}\"${NC}"
git commit -m "$COMMIT_MESSAGE"

# –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ —É—Å–ø—ñ—à–Ω–æ—Å—Ç—ñ –∫–æ–º—ñ—Ç—É
if [ $? -eq 0 ]; then
    echo -e "${GREEN}‚úÖ –ö–æ–º—ñ—Ç —Å—Ç–≤–æ—Ä–µ–Ω–æ —É—Å–ø—ñ—à–Ω–æ${NC}"
    
    # Push –¥–æ –≤—ñ–¥–¥–∞–ª–µ–Ω–æ–≥–æ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä—ñ—é
    echo -e "${BLUE}üöÄ –í—ñ–¥–ø—Ä–∞–≤–ª–µ–Ω–Ω—è –Ω–∞ GitLab...${NC}"
    git push
    
    if [ $? -eq 0 ]; then
        echo -e "${GREEN}üéâ –û–Ω–æ–≤–ª–µ–Ω–Ω—è –∑–∞–≤–µ—Ä—à–µ–Ω–æ —É—Å–ø—ñ—à–Ω–æ!${NC}"
    else
        echo -e "${RED}‚ùå –ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ –≤—ñ–¥–ø—Ä–∞–≤–ª–µ–Ω–Ω—ñ –Ω–∞ GitLab${NC}"
        exit 1
    fi
else
    echo -e "${RED}‚ùå –ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ —Å—Ç–≤–æ—Ä–µ–Ω–Ω—ñ –∫–æ–º—ñ—Ç—É${NC}"
    exit 1
fi
